public class TutorInfo {

    public String email;
    public String name;
    public String education;
    public String classes;
    public double rate;

    public TutorInfo(String email, String name, String education, String classes, double rate) {
        this.email = email;
        this.name = name;
        this.education = education;
        this.classes = classes;
        this.rate = rate;
    }
}
